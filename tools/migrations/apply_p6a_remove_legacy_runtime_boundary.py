def fail(message: str) -> None:
    raise RuntimeError(f"{PATCH_ID}: {message}")


def read(path: Path) -> str:
    if not path.is_file():
        fail(f"required file missing: {path.as_posix()}")
    return path.read_text(encoding="utf-8", errors="ignore")


def write(path: Path, content: str) -> None:
    path.write_text(content, encoding="utf-8", newline="\n")


def replace_once(content: str, old: str, new: str, label: str) -> str:
    if old not in content:
        if new in content:
            print(f"ALREADY_PATCHED={label}")
            return content
        fail(f"token not found for {label}")
    print(f"PATCHED={label}")
    return content.replace(old, new, 1)


def regex_replace_once(content: str, pattern: str, new: str, label: str) -> str:
    updated, count = re.subn(pattern, new, content, count=1, flags=re.S)
    if count == 0:
        if new.strip() in content:
            print(f"ALREADY_PATCHED={label}")
            return content
        fail(f"pattern not found for {label}")
    print(f"PATCHED={label}")
    return updated


def ensure_clean_before() -> None:
    result = subprocess.run(["git", "status", "--short"], cwd=ROOT, text=True, capture_output=True, check=False)
    if result.returncode != 0:
        fail(result.stderr.strip())
    dirty = [line for line in result.stdout.splitlines() if line.strip()]
    if dirty:
        fail("working tree must be clean before migration: " + " | ".join(dirty))


def move_application() -> None:
    if RUNTIME_APPLICATION.is_file() and not LEGACY_APPLICATION.exists():
        print("ALREADY_MOVED=Opus/Runtime/Application.php")
    else:
        if not LEGACY_APPLICATION.is_file():
            fail("legacy application source missing")
        RUNTIME_APPLICATION.parent.mkdir(parents=True, exist_ok=True)
        shutil.move(str(LEGACY_APPLICATION), str(RUNTIME_APPLICATION))
        print("MOVED=Opus/Legacy/Application/Application.class.php=>Opus/Runtime/Application.php")

    content = read(RUNTIME_APPLICATION)
    content = replace_once(
        content,
        "dirname(__DIR__, 3) . '/config/fsm.boot.php'",
        "dirname(__DIR__, 2) . '/config/fsm.boot.php'",
        "runtime_application_boot_fsm_root_depth",
    )
    write(RUNTIME_APPLICATION, content)


def remove_legacy_autoloader() -> None:
    if LEGACY_AUTOLOADER.exists():
        LEGACY_AUTOLOADER.unlink()
        print("REMOVED=Opus/Legacy/Autoload/autoloader.class.php")
    else:
        print("ALREADY_REMOVED=Opus/Legacy/Autoload/autoloader.class.php")

    for directory in [
        ROOT / "Opus/Legacy/Autoload",
        ROOT / "Opus/Legacy/Application",
        ROOT / "Opus/Legacy",
    ]:
        if directory.exists() and directory.is_dir():
            try:
                directory.rmdir()
                print(f"REMOVED_EMPTY_DIR={directory.relative_to(ROOT).as_posix()}")
            except OSError:
                print(f"DIR_NOT_EMPTY={directory.relative_to(ROOT).as_posix()}")


