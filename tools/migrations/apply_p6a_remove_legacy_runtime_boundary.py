"""
P6A_REMOVE_LEGACY_RUNTIME_BOUNDARY

Controlled migration to remove the OPUS runtime dependency on Opus/Legacy.

Contract:
- no silent fallback;
- Composer remains the only loader for www/index.php;
- OPUS_Application is kept unchanged as a runtime class and moved out of Opus/Legacy;
- Opus/Legacy is removed only after runtime references are rewritten;
- Composer autoload must be regenerated by the operator after this migration.
"""
